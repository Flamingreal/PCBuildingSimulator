// This enumeration defines the different types of PC components
// that can be used or interacted with in the simulation.

public enum e_Components
{
    CPU,          // Central Processing Unit - the "brain" of the computer.
    GPU,          // Graphics Processing Unit - responsible for rendering visuals.
    RAM,          // Random Access Memory - short-term memory for active tasks.
    Motherboard,  // The main circuit board that connects all components together.
    PowerSupply,  // Provides electrical power to the computer components.
    Storage,      // Includes SSDs and HDDs for storing data.
    Case,         // The enclosure that holds and protects all components.
    Cooler        // Cooling systems, such as air or liquid coolers.
}
